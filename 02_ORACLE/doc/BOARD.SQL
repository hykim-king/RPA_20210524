DROP TABLE BOARD;

--테이블 생성
CREATE TABLE BOARD  (						
	SEQ			NUMBER,				
	TITLE 		VARCHAR2(1000 BYTE),					
	CONSTENTS 	CLOB,					
	READ_CNT 	NUMBER DEFAULT 0,					
	REG_ID 		VARCHAR2(12 BYTE),					
	REG_DT 		DATE DEFAULT SYSDATE				
);						

--COMMENT
COMMENT ON TABLE BOARD IS '게시판';

--INDEX생성
CREATE UNIQUE INDEX PK_BOARD
 ON BOARD (
  SEQ ASC
 );

