DROP INDEX PK_BOARD_E;

/* 게시판 */
DROP TABLE BOARD_E 
	CASCADE CONSTRAINTS;

/* 게시판 */
CREATE TABLE BOARD_E (
	SEQ NUMBER NOT NULL, 
	TITLE VARCHAR2(1000 CHAR) NOT NULL, 
	CONTENTS CLOB, 
	READ_CNT NUMBER(8) DEFAULT 0, 
	REG_ID VARCHAR2(12 BYTE), 
	REG_DT DATE DEFAULT SYSDATE, 
	MOD_DT DATE 
);

COMMENT ON TABLE BOARD_E IS '게시판';

COMMENT ON COLUMN BOARD_E.SEQ IS '순번';

COMMENT ON COLUMN BOARD_E.TITLE IS '제목';

COMMENT ON COLUMN BOARD_E.CONTENTS IS '내용';

COMMENT ON COLUMN BOARD_E.READ_CNT IS '조회수';

COMMENT ON COLUMN BOARD_E.REG_ID IS '등록자ID';

COMMENT ON COLUMN BOARD_E.REG_DT IS '등록일';

COMMENT ON COLUMN BOARD_E.MOD_DT IS '수정일';

CREATE UNIQUE INDEX PK_BOARD_E
	ON BOARD_E (
		SEQ ASC
	);

ALTER TABLE BOARD_E
	ADD
		CONSTRAINT PK_BOARD_E
		PRIMARY KEY (
			SEQ
		);