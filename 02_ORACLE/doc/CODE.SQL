DROP TABLE CODE 
	CASCADE CONSTRAINTS;

/* 공통코드 */
CREATE TABLE CODE (
	MST_CODE VARCHAR2(30 BYTE) NOT NULL,
	DET_CODE VARCHAR2(30 BYTE) NOT NULL,
	DET_NM VARCHAR2(200 CHAR),
	MST_NM VARCHAR2(200 CHAR),
	ORDER NUMBER,
	USE_YN CHAR(1 BYTE) DEFAULT '0', 
	REG_ID VARCHAR2(12 BYTE),
	REG_DT DATE DEFAULT SYSDATE,
	MOD_ID VARCHAR2(12 BYTE),
	MOD_DT DATE DEFAULT SYSDATE
);

COMMENT ON TABLE CODE IS '공통코드';

COMMENT ON COLUMN CODE.MST_CODE IS '마스터코드';

COMMENT ON COLUMN CODE.DET_CODE IS '상세코드';

COMMENT ON COLUMN CODE.DET_NM IS '상세코드명';

COMMENT ON COLUMN CODE.MST_NM IS '마스터코드명';

COMMENT ON COLUMN CODE.ORDER IS '순서';

COMMENT ON COLUMN CODE.USE_YN IS '사용여부';

COMMENT ON COLUMN CODE.REG_ID IS '등록자';

COMMENT ON COLUMN CODE.REG_DT IS '등록일';

COMMENT ON COLUMN CODE.MOD_ID IS '수정자';

COMMENT ON COLUMN CODE.MOD_DT IS '수정일';

CREATE UNIQUE INDEX PK_CODE
	ON CODE (
		MST_CODE ASC,
		DET_CODE ASC
	);

ALTER TABLE CODE
	ADD
		CONSTRAINT PK_CODE
